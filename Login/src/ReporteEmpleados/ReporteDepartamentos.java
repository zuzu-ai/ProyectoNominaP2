    private void btnMostrarDepartamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarDepartamentoActionPerformed
        String dep = txtBuscarDepartamento.getText().trim();
        if (dep.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Â¡No se ingreo sl registro de reporte!", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {

            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/nominaproyect", "root", "6182");
            PreparedStatement pst = cn.prepareStatement("select * from Reporte_Departamento where Reporte_Departamentos_Id = ?");
            pst.setString(1, txtBuscarDepartamento.getText().trim());

            ResultSet rs = pst.executeQuery();
            if(rs.next()){
             
            txtDepartamentosActuales.setText(rs.getString("Departamentos_Actuales"));
            txtDepartamentosEnero.setText(rs.getString("Departamentos_Enero"));
            txtDepartamentosFebrero.setText(rs.getString("Departamentos_Febrero"));
            txtDepartamentoMarzo.setText(rs.getString("Departamentos_Marzo"));
            txtDepartamentoAbril.setText(rs.getString("Departamentos_Abril"));
            txtDepartamentoMayo.setText(rs.getString("Departamentos_Mayo"));
            txtDepartamentoJunio.setText(rs.getString("Departamentos_Junio"));
            txtDepartamentoJulio.setText(rs.getString("Departamentos_Julio"));
            txtDepartamentoAgosto.setText(rs.getString("Departamentos_Agostop"));
            txtDepartamentoSeptiembre.setText(rs.getString("Departamentos_Septiembre"));
            txtDepartamentoOctubre.setText(rs.getString("Departamentos_Octubre"));
            txtDepartamentoNoviembre.setText(rs.getString("Departamentos_Noviembre"));
            txtDepartamentoDiciembre.setText(rs.getString("Departamentos_Diciembre"));
            }else {
                JOptionPane.showMessageDialog(null, "Reporte no encontrado");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error en Monstrar Reporte");
        }
    }//GEN-LAST:event_btnMostrarDepartamentoActionPerformed

